package cl.laaraucana.simulacion.webservices.client.oficinaGastosNotarial.VO;


public class OficinaGastosNotarialSalidaVO {
	private String codigo;
	private String descripcion;
	private String gastoNotarial;

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String getGastoNotarial() {
		return gastoNotarial;
	}

	public void setGastoNotarial(String gastoNotarial) {
		this.gastoNotarial = gastoNotarial;
	}

}
