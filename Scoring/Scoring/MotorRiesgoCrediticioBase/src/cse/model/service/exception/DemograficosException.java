package cse.model.service.exception;

public class DemograficosException extends Exception {

	private static final long serialVersionUID = 8035031410917824094L;

	public DemograficosException() {
		super();
	}

	public DemograficosException(String message, Throwable cause) {
		super(message, cause);
	}

	public DemograficosException(String message) {
		super(message);
	}

	public DemograficosException(Throwable cause) {
		super(cause);
	}

}
