package cse.model.service.exception;

public class InfoLaboralException extends Exception {

	private static final long serialVersionUID = -4173742558595045474L;

	public InfoLaboralException() {
		super();
	}

	public InfoLaboralException(String message, Throwable cause) {
		super(message, cause);
	}

	public InfoLaboralException(String message) {
		super(message);
	}

	public InfoLaboralException(Throwable cause) {
		super(cause);
	}

	

}
