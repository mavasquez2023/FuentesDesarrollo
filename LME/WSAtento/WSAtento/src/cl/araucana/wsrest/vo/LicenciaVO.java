public class LicenciaVO {
	   private String codigoSucursalPago;
	   private String fechaPago;
	   private Integer compin;
	   private Integer tipo;
	   private Integer dias;
	   private String fechaDesde;
	/**
	 * @return the codigoSucursalPago
	 */
	public String getCodigoSucursalPago() {
		return codigoSucursalPago;
	}
	/**
	 * @param codigoSucursalPago the codigoSucursalPago to set
	 */
	public void setCodigoSucursalPago(String codigoSucursalPago) {
		this.codigoSucursalPago = codigoSucursalPago;
	}
	/**
	 * @return the fechaPago
	 */
	public String getFechaPago() {
		return fechaPago;
	}
	/**
	 * @param fechaPago the fechaPago to set
	 */
	public void setFechaPago(String fechaPago) {
		this.fechaPago = fechaPago;
	}
	/**
	 * @return the compin
	 */
	public Integer getCompin() {
		return compin;
	}
	/**
	 * @param compin the compin to set
	 */
	public void setCompin(Integer compin) {
		this.compin = compin;
	}
	/**
	 * @return the tipo
	 */
	public Integer getTipo() {
		return tipo;
	}
	/**
	 * @param tipo the tipo to set
	 */
	public void setTipo(Integer tipo) {
		this.tipo = tipo;
	}
	/**
	 * @return the dias
	 */
	public Integer getDias() {
		return dias;
	}
	/**
	 * @param dias the dias to set
	 */
	public void setDias(Integer dias) {
		this.dias = dias;
	}
	/**
	 * @return the fechaDesde
	 */
	public String getFechaDesde() {
		return fechaDesde;
	}
	/**
	 * @param fechaDesde the fechaDesde to set
	 */
	public void setFechaDesde(String fechaDesde) {
		this.fechaDesde = fechaDesde;
	}
	    
	    
	    
}
