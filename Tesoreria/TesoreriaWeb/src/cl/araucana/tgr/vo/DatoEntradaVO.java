public class DatoEntradaVO {
private int rutemp;
private int anio;
private String mes;
private int ruttra;
/**
 * @return the rutemp
 */
public int getRutemp() {
	return rutemp;
}
/**
 * @param rutemp the rutemp to set
 */
public void setRutemp(int rutemp) {
	this.rutemp = rutemp;
}
/**
 * @return the anio
 */
public int getAnio() {
	return anio;
}
/**
 * @param anio the anio to set
 */
public void setAnio(int anio) {
	this.anio = anio;
}
/**
 * @return the mes
 */
public String getMes() {
	return mes;
}
/**
 * @param mes the mes to set
 */
public void setMes(String mes) {
	this.mes = mes;
}
/**
 * @return the ruttra
 */
public int getRuttra() {
	return ruttra;
}
/**
 * @param ruttra the ruttra to set
 */
public void setRuttra(int ruttra) {
	this.ruttra = ruttra;
}



}
