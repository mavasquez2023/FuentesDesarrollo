package cl.araucana.sivegam.vo;

public class PeriodoAniosVO {

    /* variables de la clase. */
    private int codigo_anio;
    private String glosa_anio;

    /* generacion de get and set */
    public int getCodigo_anio() {
        return codigo_anio;
    }

    public void setCodigo_anio(int codigo_anio) {
        this.codigo_anio = codigo_anio;
    }

    public String getGlosa_anio() {
        return glosa_anio;
    }

    public void setGlosa_anio(String glosa_anio) {
        this.glosa_anio = glosa_anio;
    }

}
