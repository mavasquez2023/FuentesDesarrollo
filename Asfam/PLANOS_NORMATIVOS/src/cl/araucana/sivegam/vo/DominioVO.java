package cl.araucana.sivegam.vo;

public class DominioVO {

    /** Variables de la clase */
    private String id_dominio;
    private String des_dominio;
    private String clave_dominio;

    public String getClave_dominio() {
        return clave_dominio;
    }

    public void setClave_dominio(String clave_dominio) {
        this.clave_dominio = clave_dominio;
    }

    public String getDes_dominio() {
        return des_dominio;
    }

    public void setDes_dominio(String des_dominio) {
        this.des_dominio = des_dominio;
    }

    public String getId_dominio() {
        return id_dominio;
    }

    public void setId_dominio(String id_dominio) {
        this.id_dominio = id_dominio;
    }

}
