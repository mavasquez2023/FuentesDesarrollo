

package cl.araucana.wssiagf;


public class WSSIAGFException extends Exception {

	public WSSIAGFException() {
		super();
	}

	public WSSIAGFException(String message) {
		super(message);
	}

	public WSSIAGFException(Throwable cause) {
		super(cause);
	}

	public WSSIAGFException(String message, Throwable cause) {
		super(message, cause);
	}
}
