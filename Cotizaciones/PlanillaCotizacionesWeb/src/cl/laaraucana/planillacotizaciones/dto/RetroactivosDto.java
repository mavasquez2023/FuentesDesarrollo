package cl.laaraucana.planillacotizaciones.dto;

public class RetroactivosDto {

	private String oficina;
	private String rutem;
	private String sucursal;
	private String periodo;
	private String cnormal;
	private String monto;

	public String getOficina() {
		return oficina;
	}

	public void setOficina(String oficina) {
		this.oficina = oficina;
	}

	public String getRutem() {
		return rutem;
	}

	public void setRutem(String rutem) {
		this.rutem = rutem;
	}

	public String getSucursal() {
		return sucursal;
	}

	public void setSucursal(String sucursal) {
		this.sucursal = sucursal;
	}

	public String getPeriodo() {
		return periodo;
	}

	public void setPeriodo(String periodo) {
		this.periodo = periodo;
	}

	public String getCnormal() {
		return cnormal;
	}

	public void setCnormal(String cnormal) {
		this.cnormal = cnormal;
	}

	public String getMonto() {
		return monto;
	}

	public void setMonto(String monto) {
		this.monto = monto;
	}

}
