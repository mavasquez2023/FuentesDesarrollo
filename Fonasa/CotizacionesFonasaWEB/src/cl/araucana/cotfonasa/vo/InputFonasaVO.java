package cl.araucana.cotfonasa.vo;

public class InputFonasaVO {


	private long rutCotizante;
	private String dvCotizante;
	private long rutEmpleador;
	private String dvEmpleador;
	private long periodo;
	
	
	public long getRutCotizante() {
		return rutCotizante;
	}
	public void setRutCotizante(long rutCotizante) {
		this.rutCotizante = rutCotizante;
	}
	public String getDvCotizante() {
		return dvCotizante;
	}
	public void setDvCotizante(String dvCotizante) {
		this.dvCotizante = dvCotizante;
	}
	public long getRutEmpleador() {
		return rutEmpleador;
	}
	public void setRutEmpleador(long rutEmpleador) {
		this.rutEmpleador = rutEmpleador;
	}
	public String getDvEmpleador() {
		return dvEmpleador;
	}
	public void setDvEmpleador(String dvEmpleador) {
		this.dvEmpleador = dvEmpleador;
	}
	public long getPeriodo() {
		return periodo;
	}
	public void setPeriodo(long periodo) {
		this.periodo = periodo;
	}
	
	

}
