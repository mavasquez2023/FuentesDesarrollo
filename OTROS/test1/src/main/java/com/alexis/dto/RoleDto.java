package com.alexis.dto;


public class RoleDto {
	
	public RoleDto(long _idRol, String _role) {
		
		this.idRol = _idRol;
		this.role = _role;
	};
	
	
	private long idRol;
	private String role;
	
	
	public long getIdRol() {
		return idRol;
	}
	public void setIdRol(long idRol) {
		this.idRol = idRol;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	
	
	

}
