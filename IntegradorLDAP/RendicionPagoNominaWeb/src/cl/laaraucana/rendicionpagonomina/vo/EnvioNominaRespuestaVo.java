package cl.laaraucana.rendicionpagonomina.vo;

public class EnvioNominaRespuestaVo {

	String numNomina;
	String codigoRetorno;
	String nombreNomina;
	String glsErrorEnvio;
	/**
	 * @return the numNomina
	 */
	public String getNumNomina() {
		return numNomina;
	}
	/**
	 * @param numNomina the numNomina to set
	 */
	public void setNumNomina(String numNomina) {
		this.numNomina = numNomina;
	}
	/**
	 * @return the codigoRetorno
	 */
	public String getCodigoRetorno() {
		return codigoRetorno;
	}
	/**
	 * @param codigoRetorno the codigoRetorno to set
	 */
	public void setCodigoRetorno(String codigoRetorno) {
		this.codigoRetorno = codigoRetorno;
	}
	/**
	 * @return the nombreNomina
	 */
	public String getNombreNomina() {
		return nombreNomina;
	}
	/**
	 * @param nombreNomina the nombreNomina to set
	 */
	public void setNombreNomina(String nombreNomina) {
		this.nombreNomina = nombreNomina;
	}
	/**
	 * @return the glsErrorEnvio
	 */
	public String getGlsErrorEnvio() {
		return glsErrorEnvio;
	}
	/**
	 * @param glsErrorEnvio the glsErrorEnvio to set
	 */
	public void setGlsErrorEnvio(String glsErrorEnvio) {
		this.glsErrorEnvio = glsErrorEnvio;
	}
	
	
	
}
