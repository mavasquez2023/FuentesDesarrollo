package cl.laaraucana.pubnominas.dto.cotizacion;

public class CabeceraDto {

	private String tipo;
	private String codBar;
	private String codofi;
	private String razonsocial;
	private String rutEmpresa;
	private String digEmpresa;
	private String sucursal;
	private String periodo;
	private String nomSucursal;
	private String tipoDeclaracion;

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public String getCodBar() {
		return codBar;
	}

	public void setCodBar(String codBar) {
		this.codBar = codBar;
	}

	public String getCodofi() {
		return codofi;
	}

	public void setCodofi(String codofi) {
		this.codofi = codofi;
	}

	public String getRazonsocial() {
		return razonsocial;
	}

	public void setRazonsocial(String razonsocial) {
		this.razonsocial = razonsocial;
	}

	public String getRutEmpresa() {
		return rutEmpresa;
	}

	public void setRutEmpresa(String rutEmpresa) {
		this.rutEmpresa = rutEmpresa;
	}

	public String getDigEmpresa() {
		return digEmpresa;
	}

	public void setDigEmpresa(String digEmpresa) {
		this.digEmpresa = digEmpresa;
	}

	public String getSucursal() {
		return sucursal;
	}

	public void setSucursal(String sucursal) {
		this.sucursal = sucursal;
	}

	public String getPeriodo() {
		return periodo;
	}

	public void setPeriodo(String periodo) {
		this.periodo = periodo;
	}

	public String getNomSucursal() {
		return nomSucursal;
	}

	public void setNomSucursal(String nomSucursal) {
		this.nomSucursal = nomSucursal;
	}

	/**
	 * @return the tipoDeclaracion
	 */
	public String getTipoDeclaracion() {
		return tipoDeclaracion;
	}

	/**
	 * @param tipoDeclaracion the tipoDeclaracion to set
	 */
	public void setTipoDeclaracion(String tipoDeclaracion) {
		this.tipoDeclaracion = tipoDeclaracion;
	}
	
	
}
