package cl.laaraucana.pubnominas.dto.cotizacion;

public class CargasDto {

	private String codofi;
	private String rutem;
	private String sucursal;
	private String periodo;
	private String tramo;
	private String cnormales;
	private String invalidas;

	public String getCodofi() {
		return codofi;
	}

	public void setCodofi(String codofi) {
		this.codofi = codofi;
	}

	public String getRutem() {
		return rutem;
	}

	public void setRutem(String rutem) {
		this.rutem = rutem;
	}

	public String getSucursal() {
		return sucursal;
	}

	public void setSucursal(String sucursal) {
		this.sucursal = sucursal;
	}

	public String getPeriodo() {
		return periodo;
	}

	public void setPeriodo(String periodo) {
		this.periodo = periodo;
	}

	public String getTramo() {
		return tramo;
	}

	public void setTramo(String tramo) {
		this.tramo = tramo;
	}

	public String getCnormales() {
		return cnormales;
	}

	public void setCnormales(String cnormales) {
		this.cnormales = cnormales;
	}

	public String getInvalidas() {
		return invalidas;
	}

	public void setInvalidas(String invalidas) {
		this.invalidas = invalidas;
	}

}
