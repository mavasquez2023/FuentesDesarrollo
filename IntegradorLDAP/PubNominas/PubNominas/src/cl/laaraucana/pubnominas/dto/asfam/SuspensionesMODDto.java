package cl.laaraucana.pubnominas.dto.asfam;

public class SuspensionesMODDto {
	private String rutAfiliado;
	private String nombreAfiliado;
	private String afiliadosVigente;
	private String nombreCausante;
	private String motivo;
	/**
	 * @return the rutAfiliado
	 */
	public String getRutAfiliado() {
		return rutAfiliado;
	}
	/**
	 * @param rutAfiliado the rutAfiliado to set
	 */
	public void setRutAfiliado(String rutAfiliado) {
		this.rutAfiliado = rutAfiliado;
	}
	/**
	 * @return the nombreAfiliado
	 */
	public String getNombreAfiliado() {
		return nombreAfiliado;
	}
	/**
	 * @param nombreAfiliado the nombreAfiliado to set
	 */
	public void setNombreAfiliado(String nombreAfiliado) {
		this.nombreAfiliado = nombreAfiliado;
	}
	/**
	 * @return the afiliadosVigente
	 */
	public String getAfiliadosVigente() {
		return afiliadosVigente;
	}
	/**
	 * @param afiliadosVigente the afiliadosVigente to set
	 */
	public void setAfiliadosVigente(String afiliadosVigente) {
		this.afiliadosVigente = afiliadosVigente;
	}
	/**
	 * @return the nombreCausante
	 */
	public String getNombreCausante() {
		return nombreCausante;
	}
	/**
	 * @param nombreCausante the nombreCausante to set
	 */
	public void setNombreCausante(String nombreCausante) {
		this.nombreCausante = nombreCausante;
	}
	/**
	 * @return the motivo
	 */
	public String getMotivo() {
		return motivo;
	}
	/**
	 * @param motivo the motivo to set
	 */
	public void setMotivo(String motivo) {
		this.motivo = motivo;
	}
	
	
}
