package cl.laaraucana.autoasfam.dto;

public class PaginasMODDto {
	private int cantidad;
	private int paginas;
	private int paginaAdicional;
	/**
	 * @return the cantidad
	 */
	public int getCantidad() {
		return cantidad;
	}
	/**
	 * @param cantidad the cantidad to set
	 */
	public void setCantidad(int cantidad) {
		this.cantidad = cantidad;
	}
	/**
	 * @return the paginas
	 */
	public int getPaginas() {
		return paginas;
	}
	/**
	 * @param paginas the paginas to set
	 */
	public void setPaginas(int paginas) {
		this.paginas = paginas;
	}
	/**
	 * @return the paginaAdicional
	 */
	public int getPaginaAdicional() {
		return paginaAdicional;
	}
	/**
	 * @param paginaAdicional the paginaAdicional to set
	 */
	public void setPaginaAdicional(int paginaAdicional) {
		this.paginaAdicional = paginaAdicional;
	}
	
	
}
