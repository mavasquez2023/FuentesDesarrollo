package cl.laaraucana.ventaremota.model;

public class CreditoViewVo {

	private String idCredito;
	private String tipoCreditoNormal;
	private String fechaOtorgamiento;
	private String montoSolicitado;
	private String montoCuota;
	private String numeroCuotas;
	private String url;
	private String name;

	
	
	public String getIdCredito() {
		return idCredito;
	}

	public void setIdCredito(String idCredito) {
		this.idCredito = idCredito;
	}

	public String getTipoCreditoNormal() {
		return tipoCreditoNormal;
	}

	public void setTipoCreditoNormal(String tipoCreditoNormal) {
		this.tipoCreditoNormal = tipoCreditoNormal;
	}

	public String getFechaOtorgamiento() {
		return fechaOtorgamiento;
	}

	public void setFechaOtorgamiento(String fechaOtorgamiento) {
		this.fechaOtorgamiento = fechaOtorgamiento;
	}

	public String getMontoSolicitado() {
		return montoSolicitado;
	}

	public void setMontoSolicitado(String montoSolicitado) {
		this.montoSolicitado = montoSolicitado;
	}

	public String getMontoCuota() {
		return montoCuota;
	}

	public void setMontoCuota(String montoCuota) {
		this.montoCuota = montoCuota;
	}

	public String getNumeroCuotas() {
		return numeroCuotas;
	}

	public void setNumeroCuotas(String numeroCuotas) {
		this.numeroCuotas = numeroCuotas;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	
	
}
