package cl.laaraucana.recepcionsil.service.vo;

public class MixtaValidacionVO {
	private String fechaProceso;
	private String fechaRespuesta;
	private String horaRespuesta;
	private String codigoRespuesta;
	private String tipoValidacion;
	private String glosaValidacion;

	public String getFechaProceso() {
		return fechaProceso;
	}

	public void setFechaProceso(String fechaProceso) {
		this.fechaProceso = fechaProceso;
	}

	public String getFechaRespuesta() {
		return fechaRespuesta;
	}

	public void setFechaRespuesta(String fechaRespuesta) {
		this.fechaRespuesta = fechaRespuesta;
	}

	public String getHoraRespuesta() {
		return horaRespuesta;
	}

	public void setHoraRespuesta(String horaRespuesta) {
		this.horaRespuesta = horaRespuesta;
	}

	public String getCodigoRespuesta() {
		return codigoRespuesta;
	}

	public void setCodigoRespuesta(String codigoRespuesta) {
		this.codigoRespuesta = codigoRespuesta;
	}

	public String getTipoValidacion() {
		return tipoValidacion;
	}

	public void setTipoValidacion(String tipoValidacion) {
		this.tipoValidacion = tipoValidacion;
	}

	public String getGlosaValidacion() {
		return glosaValidacion;
	}

	public void setGlosaValidacion(String glosaValidacion) {
		this.glosaValidacion = glosaValidacion;
	}

}
