package cl.laaraucana.recepcionsil.service.vo;

import java.io.Serializable;

/**
 * @author  J-Factory Solutions
 *
 */
public class ReposoVO implements Serializable{
	//A4 Características del Reposo
	private String tipoReposo;
	private String jornadaReposo;
	private String lugarReposo;
	private String justificarOtro;
	private String direccionReposo;
	private String telefonoReposo;
	/**
	 * @return the tipoReposo
	 */
	public String getTipoReposo() {
		return tipoReposo;
	}
	/**
	 * @param tipoReposo the tipoReposo to set
	 */
	public void setTipoReposo(String tipoReposo) {
		this.tipoReposo = tipoReposo;
	}
	/**
	 * @return the jornadaReposo
	 */
	public String getJornadaReposo() {
		return jornadaReposo;
	}
	/**
	 * @param jornadaReposo the jornadaReposo to set
	 */
	public void setJornadaReposo(String jornadaReposo) {
		this.jornadaReposo = jornadaReposo;
	}
	/**
	 * @return the lugarReposo
	 */
	public String getLugarReposo() {
		return lugarReposo;
	}
	/**
	 * @param lugarReposo the lugarReposo to set
	 */
	public void setLugarReposo(String lugarReposo) {
		this.lugarReposo = lugarReposo;
	}
	/**
	 * @return the justificarOtro
	 */
	public String getJustificarOtro() {
		return justificarOtro;
	}
	/**
	 * @param justificarOtro the justificarOtro to set
	 */
	public void setJustificarOtro(String justificarOtro) {
		this.justificarOtro = justificarOtro;
	}
	/**
	 * @return the direccionReposo
	 */
	public String getDireccionReposo() {
		return direccionReposo;
	}
	/**
	 * @param direccionReposo the direccionReposo to set
	 */
	public void setDireccionReposo(String direccionReposo) {
		this.direccionReposo = direccionReposo;
	}
	/**
	 * @return the telefonoReposo
	 */
	public String getTelefonoReposo() {
		return telefonoReposo;
	}
	/**
	 * @param telefonoReposo the telefonoReposo to set
	 */
	public void setTelefonoReposo(String telefonoReposo) {
		this.telefonoReposo = telefonoReposo;
	}
	
	
}
